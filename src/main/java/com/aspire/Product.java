package com.aspire;

public class Product {
	
	int value;
	String productname;
	
	public Product(int value, String productname) {
		super();
		this.value = value;
		this.productname = productname;
	}

	public int getValue() {
		return value;
	}

	public String getProductname() {
		return productname;
	}
	
	
	

}
